package com.kodasfiledata.parameter;

public class SectorParameter {
	private String dtm;
	private String sort;
	private String file_nm;
	private String sen_tp;
	private String pstn_no;
	
	
	public String getPstn_no() {
		return pstn_no;
	}
	public void setPstn_no(String pstn_no) {
		this.pstn_no = pstn_no;
	}
	public String getSen_tp() {
		return sen_tp;
	}
	public void setSen_tp(String sen_tp) {
		this.sen_tp = sen_tp;
	}
	public String getFile_nm() {
		return file_nm;
	}
	public void setFile_nm(String file_nm) {
		this.file_nm = file_nm;
	}
	public String getDtm() {
		return dtm;
	}
	public void setDtm(String dtm) {
		this.dtm = dtm;
	}
	public String getSort() {
		return sort;
	}
	public void setSort(String sort) {
		this.sort = sort;
	}
	
	

}
